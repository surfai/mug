def GenerateInstruct(input_2: str) -> str:
    """
    Append " to GenerateInstruct" to the given input string.

    This function takes an input string and appends the phrase " to GenerateInstruct"
    to the end of it.

    Args:
        input_2 (str): The input string to be modified.

    Returns:
        str: A new string consisting of the input string followed by " to GenerateInstruct".

    Example:
        >>> GenerateInstruct("Hello")
        'Hello to GenerateInstruct'
    """
    return input_2 + " to GenerateInstruct"
